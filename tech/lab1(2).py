a = float(input("Enter first number ",))
b = float(input("Enter secont number ",))

sum = abs(a) + abs(b)
diff = abs(a) - abs(b)
prod = abs(a) * abs(b)
div = abs(a) / abs(b)

print("Sum of moduls = {:.3f}".format(sum))
print("Difference of moduls = {:.3f}".format(diff))
print("Product of moduls = {:.3f}".format(prod))
print("Division of moduls = {:.3f}".format(div))
